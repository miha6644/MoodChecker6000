mood = input("What mood are you in today? ")

if mood == "Happy":
    print("It's great to see you happy!")
elif mood == "Nervous":
    print("Take a breath 3 times.")
elif mood == "Sad":
    print("Chin up, champ!")
elif mood == "Excited":
    print("Woohoo!")
elif mood == "Relaxed":
    print ("Rasta, bruh...")
else:
    print("I don't recognize this mood.")